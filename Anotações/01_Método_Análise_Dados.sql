PRINCÍPIO DE PARETO
"80%  DAS TERRAS SÃO DE PROPRIEDADE DE 20% DA POPULAÇÃO"

ISSO TRAZ A IDEIA DE QUE OS RESULTADOS SÃO MOTIVADOS QUASE SEMPRE PELAS 20% DAS CAUSAS PRINCIPAIS, PORTANTO UMA QUANTIDADE PEQUENA DE FATORES INFLUÊNCIA NO RESULTADO MAIOR 

"80% PIB É PROVENIENTES DE 20% DO PAÍS"

Então basicamente o Princípio de Pareto é poderoso pra análise e tomada de
decisões, porque aqui a gente consegue focar nos 20%, ou seja, focar nos fatores
mais importantes, e com isso conseguir a maior parte dos resultados (80%), com
menos tempo e esforço.

FOQUE NOS PRINCIPAIS INDICADORES PARA TOMADA DE DECISÃO

